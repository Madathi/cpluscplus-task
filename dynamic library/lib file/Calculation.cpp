#include "pch.h"
#include<utility>
#include<limits.h>
#include "Calculation.h"

double Add(double a, double b)
{
	return a + b;
}
double Sub(double a, double b)
{
	return a - b;
}
double Multiply(double a, double b)
{
	return a * b;
}
double Division(double a, double b)
{
	return a / b;
}